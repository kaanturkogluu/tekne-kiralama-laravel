<script>
    // Toplu seçim işlemleri
    document.addEventListener('DOMContentLoaded', function() {
        const selectAllCheckbox = document.querySelector('thead input[type="checkbox"]');
        const userCheckboxes = document.querySelectorAll('tbody input[type="checkbox"]');

        // Tümünü seç/kaldır
        selectAllCheckbox.addEventListener('change', function() {
            userCheckboxes.forEach(checkbox => {
                checkbox.checked = this.checked;
            });
        });

        // Tekil checkbox değişikliklerini takip et
        userCheckboxes.forEach(checkbox => {
            checkbox.addEventListener('change', function() {
                const checkedCount = document.querySelectorAll('tbody input[type="checkbox"]:checked').length;
                const totalCount = userCheckboxes.length;
                
                if (checkedCount === totalCount) {
                    selectAllCheckbox.checked = true;
                } else if (checkedCount === 0) {
                    selectAllCheckbox.checked = false;
                } else {
                    selectAllCheckbox.indeterminate = true;
                }
            });
        });
    });
</script>
